package org.TestScriptData;

public class AppiumSetupData {

    private String appiumPath;
    private String ipAddress;
    private int port;
    private String emulatorDeviceName;
    private String sysProperty;
    private String apkPath;


    public String getAppiumPath() {
        return appiumPath;
    }

    public String getIpAddress() {
        return ipAddress;
    }

    public int getPort() {
        return port;
    }

    public String getEmulatorDeviceName() {
        return emulatorDeviceName;
    }

    public String getSysProperty() {
        return sysProperty;
    }

    public String getApkPath() {
        return apkPath;
    }
}
